public class Conversion {

    public double convertMmToCm(double mm) {
        return mm / 10;
    }

    public double convertCmToMm(double cm) {
        return cm * 10;
    }

    public double convertCmToM(double cm) {
        return cm / 100;
    }

    public double convertMToCm(double m) {
        return m * 100;
    }

}
